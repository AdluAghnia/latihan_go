package example

import "fmt"

func DataTypeExample() {
  // Jenis-jenis tipe data sederhana di Go
  var a bool =  true
  var b int = 5
  var c float32 = 3.14
  var d string = "Hi Hallo"

  fmt.Println("Boolean = ", a)
  fmt.Println("Integer = ", b)
  fmt.Println("Float = ", c)
  fmt.Println("String = ", d)
}


